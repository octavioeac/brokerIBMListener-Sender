package com.frida.pojoXML;

public class PsPuerto {

	private String NombrePuerto;
	
	private String IDPuerto;
	
	private String IDPuertoFRIDA;
	
	
	public String getNombrePuerto() {
		return NombrePuerto;
	}
	public void setNombrePuerto(String nombrePuerto) {
		NombrePuerto = nombrePuerto;
	}
	public String getIDPuerto() {
		return IDPuerto;
	}
	public void setIDPuerto(String iDPuerto) {
		IDPuerto = iDPuerto;
	}
	public String getIDPuertoFRIDA() {
		return IDPuertoFRIDA;
	}
	public void setIDPuertoFRIDA(String iDPuertoFRIDA) {
		IDPuertoFRIDA = iDPuertoFRIDA;
	}
}
